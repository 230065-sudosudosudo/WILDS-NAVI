
# app.py
# -*- coding: utf-8 -*-
import streamlit as st

st.set_page_config(page_title="CFO/WH 攻略ポータル（仮）", layout="wide")
st.title("ゲーム攻略ポータル（スターター）")
st.write(
    "このサイトは、武器データ（例: 大剣.xlsx）からランキングや検索を提供する**攻略サイトの雛形**です。"
)

st.markdown('''
### ページ一覧
- **武器検索**: 日本語で質問して該当データを抽出（例: *火属性で強い武器*）
- **属性別ランキング**: 属性ごとに強い/弱いランキングを表示
- **比較ツール**: 複数武器を並べて比較
- **データ更新ガイド**: データ構造・更新手順の説明

> データは「サイドバー → データロード」から読み込めます。
''')

with st.expander("サイトの使い方（初めての方へ）", expanded=True):
    st.write('''
1. サイドバーの **データの読み込み** で `大剣.xlsx` をアップロード（または同梱ファイルを使用）。  
2. **武器検索** ページで、自然文クエリ（例:「上位で会心率が高い」）を入力して抽出。  
3. **属性別ランキング** で強い/弱いを一括確認。  
4. **比較ツール** で候補武器を並べて比較。  
''')
